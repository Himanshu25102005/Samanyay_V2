export default function I18nProvider({ children }) {
  // Start with English during SSR to avoid hydration mismatch; hydrate actual language after mount
  const [lang, setLang] = useState('English');

  useEffect(() => {
    // Initialize from storage after mount to keep SSR and first client render in sync
    setLang(getStoredLanguage());
    const h = (e) => setLang(e?.detail || getStoredLanguage());
    window.addEventListener('app-language-change', h);
    return () => window.removeEventListener('app-language-change', h);
  }, []);

  const t = useMemo(() => {
    const dict = DICT[lang] || DICT.English;
    return (key) => dict[key] ?? key;
  }, [lang]);

  return (
    <I18nContext.Provider value={{ t, lang, setLang }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() { return useContext(I18nContext); }

// Utility function to get font class based on language
export function getFontClass(language) {
  switch (language) {
    case 'Hindi':
      return 'font-hindi';
    case 'Marathi':
      return 'font-marathi';
    case 'Gujarati':
      return 'font-gujarati';
    default:
      return 'font-english';
  }
}



